def _stamp_line(edge_id: str, pass_id: str, kind: str, cofire: float) -> str:
    return f"<!-- dream: generated {kind} · pass={pass_id} · edge={edge_id} · cofire={cofire:.2f} -->"


def proposals_ledger_path(telemetry_dir: str, pass_id: str) -> str:
    return os.path.join(dream_dir(telemetry_dir), f"proposals-{pass_id}.jsonl")


def write_proposals_ledger(telemetry_dir: str, pass_id: str, proposals: dict) -> Optional[str]:
    """Persist the pass's proposals (staged or not) under the DERIVED dream dir (inv1).
    Written only when there is at least one proposal. Never raises."""
    rows = (proposals.get("schemas") or []) + (proposals.get("hypotheses") or [])
    if not rows:
        return None
    try:
        os.makedirs(dream_dir(telemetry_dir), exist_ok=True)
        path = proposals_ledger_path(telemetry_dir, pass_id)
        stamp = datetime.now(timezone.utc).isoformat(timespec="seconds")
        with open(path, "w", encoding="utf-8") as fh:
            for row in rows:
                fh.write(json.dumps({"pass": pass_id, **row, "generated_at": stamp}, ensure_ascii=False) + "\n")
        return path
    except Exception:
        return None


def _prior_decided_children(memory_dir: str) -> Dict[frozenset, str]:
    """Children-sets with a STANDING VERDICT — undone, archived, or expired — plus live
    ones (active/graduated). The ping-pong guard: a cluster the owner (or the horizon)
    already rejected is never auto re-staged; a cluster already staged is not duplicated."""
    out: Dict[frozenset, str] = {}
    for row in generated_rows(memory_dir):
        children = row.get("children")
        if not isinstance(children, list) or not children:
            continue
        key = frozenset(str(c) for c in children)
        state = row.get("state")
        if state in ("undone", "archived"):
            out[key] = str(state)
        elif state == "active":
            if row.get("expired"):
                out[key] = "expired"
            elif row.get("confidence") == "verified":
                out[key] = "graduated"
            else:
                out[key] = "staged"
    return out


def stage_generated(
    memory_dir: str,
    proposals: dict,
    *,
    pass_id: str,
    telemetry_dir: Optional[str] = None,
    index_dir: Optional[str] = None,
    repo_root: Optional[str] = None,
) -> dict:
    """Stage proposals into the corpus as ``confidence: draft`` memories. Capped, hard
    secret-BLOCKed, stamped, ledgered, trust-folded, undoable. Returns
    ``{"staged": [ledger rows], "refused": [(proposal, reason)]}``. Never raises.

    Every staged memory is created through ``new_memory.write_memory`` (the ONE canonical
    create primitive: refuses overwrite, folds SEC-6 authorship-consent, refreshes the
    index) with the confidence argument HARD-CODED to ``"draft"`` — the created-only-at-
    draft acceptance criterion lives here, not in the proposal data.
    """
    from .dream import read_apply_ledger
    from .new_memory import write_memory
    from .secrets import scan_text
    from .trust import file_sha256

    td = telemetry_dir or default_telemetry_dir(memory_dir)
    out: dict = {"staged": [], "refused": []}
    ordered = (proposals.get("schemas") or []) + (proposals.get("hypotheses") or [])
    if not ordered:
        return out
    cap = max_generate_per_pass()
    soak = soak_status(td, memory_dir=memory_dir)
    distinct_now = int(soak.get("distinct_sessions") or 0)
    session_id = current_session_id(td)
    now_iso = datetime.now(timezone.utc).isoformat(timespec="seconds")
    prior = _prior_decided_children(memory_dir)
    # Edge-id continuation: pass ids have 1-second resolution, so two stagings inside
    # one second share a pass_id — the -gN counter continues past every id the ledger
    # already carries (an id collision would MERGE rows in read_apply_ledger).
    taken = {r.get("edge_id") for r in read_apply_ledger(memory_dir)}

    def _next_edge_id(n_staged: int) -> str:
        n = n_staged + 1
        while f"{pass_id}-g{n}" in taken:
            n += 1
        return f"{pass_id}-g{n}"

    ledger_rows: List[dict] = []
    staged_paths: List[str] = []
    for prop in ordered:
        if len(ledger_rows) >= cap:
            break
        kind = prop.get("kind")
        name = prop.get("name") or ""
        children = [str(c) for c in (prop.get("children") or [])]
        if kind not in _GENERATED_KINDS or not name or len(children) < 2:
            out["refused"].append((prop, "malformed proposal (kind/name/children)"))
            continue
        verdict = prior.get(frozenset(children))
        if verdict in ("undone", "archived", "expired"):
            out["refused"].append(
                (prop, f"this cluster was {verdict} before — never auto re-staged "
                       "(stage by hand if genuinely wanted)")
            )
            continue
        if verdict in ("staged", "graduated"):
            out["refused"].append((prop, f"a generated memory over this cluster is already {verdict}"))
            continue

        edge_id = _next_edge_id(len(ledger_rows))
        cofire = float(prop.get("cofire") or 0.0)
        body = (prop.get("body") or "").rstrip("\n") + "\n\n" + _stamp_line(edge_id, pass_id, kind, cofire) + "\n"
        ledger_row = {
            "edge_id": edge_id,
            "pass": pass_id,
            "kind": kind,
            "memory": name,
            "file": f"{name}.md",
            "children": children,
            "derives_from": children,
            "cofire": cofire,
            "confidence": "draft",
            "firing_query": (prop.get("queries") or [""])[0],
            "applied_at_session": session_id,
            "applied_at_distinct_count": distinct_now,
            "applied_at_ts": now_iso,
            "state": "active",
        }
        # Provenance completeness is a hard precondition (the DRM-2 discipline).
        if not all(ledger_row.get(k) not in (None, "") for k in ("edge_id", "pass", "kind", "memory")):
            out["refused"].append((prop, "incomplete provenance"))
            continue
        # HARD secret BLOCK over every generated byte — file content AND ledger row
        # (the ratified dream-write-path deviation: REFUSED, not warned).
        rationale = "\n".join(
            [name, prop.get("description") or "", body, json.dumps(ledger_row, ensure_ascii=False)]
        )
        findings = scan_text(rationale)
        if findings:
            out["refused"].append((prop, f"secret lint BLOCK: {'; '.join(findings)}"))
            continue

        res = write_memory(
            name,
            prop.get("description") or "",
            "project",  # never floor material (only user/feedback reach MEMORY.md)
            body=body,
            memory_dir=memory_dir,
            repo_root=repo_root,
            confidence="draft",  # HARD-CODED: created ONLY at draft (DREAM-KILL-1)
            origin=f"dream:{pass_id}",
            no_links=True,  # the body's [[children]] ARE the links; no GRA-3 discovery
        )
        if not res.get("created") or not res.get("path"):
            out["refused"].append((prop, f"write_memory refused: {res.get('error') or 'unknown'}"))
            continue
        path = res["path"]
        staged_paths.append(path)

        # Typed derivation provenance: the parent declares its children (derives-from,
        # the DRM-6 relation). Additive, idempotent, per-item — links.add_typed_relation
        # folds each write into the SEC-6 consent baseline itself.
        from .links import add_typed_relation

        for child in children:
            add_typed_relation(path, "derives-from", child)

        # cited_paths: the union of the children's cited files is the draft's OUTCOME
        # surface (injection_hits joins on it — no cited paths, no graduation evidence).
        # write_memory's own backfill derives body-named paths that EXIST in the repo;
        # when it found some, its validated list wins. Missing or empty (non-git /
        # hermetic corpora, or evidence paths outside this repo), set the union
        # explicitly — a draft with no outcome surface could never graduate.
        try:
            cited = [str(c) for c in (prop.get("cited_paths") or [])]
            if cited:
                with open(path, "r", encoding="utf-8") as fh:
                    text = fh.read()
                fm_now = parse_frontmatter(text) or {}
                meta_now = fm_now.get("metadata") if isinstance(fm_now.get("metadata"), dict) else {}
                existing_cited = fm_now.get("cited_paths")
                if existing_cited is None:
                    existing_cited = (meta_now or {}).get("cited_paths")
                if not existing_cited:
                    _set_cited_paths(path, cited)
        except Exception:
            pass

        # Fold the FINAL bytes (post typed-edges, post cited-paths) into the consent
        # baseline, and record that hash as the undo oracle: undo deletes the file only
        # if it is byte-identical to what staging left — a hand-edited or graduated
        # draft refuses (protect the human's/evidence's work; use git from there).
        try:
            from .trust import record_authored_write

            record_authored_write(memory_dir, path, repo_root)
        except Exception:
            pass
        h = file_sha256(path)
        ledger_row["undo"] = {"file": f"{name}.md", "created": True, "sha256": h}
        ledger_rows.append(ledger_row)
        out["staged"].append(ledger_row)

    if ledger_rows:
        try:
            with open(apply_ledger_path(memory_dir), "a", encoding="utf-8") as fh:
                for row in ledger_rows:
                    fh.write(json.dumps(row, ensure_ascii=False) + "\n")
        except Exception as exc:
            # A ledger append failure would orphan stamps — roll the staging back.
            for p in staged_paths:
                try:
                    os.remove(p)
                except Exception:
                    pass
            out["staged"] = []
            out["refused"].append(({"name": "(pass)"}, f"ledger append FAILED ({exc}) — staging rolled back"))
            return out
        _refresh_index_quiet(memory_dir, index_dir)
    return out


