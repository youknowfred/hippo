"""/dream's generative payload — DRM-6: schema/gist + hypotheses (Tier B, quarantined).

The edge backbone (DRM-1..5) CONNECTS existing memories; this module is the tier that
creates net-new ones: **schema/gist** parents abstracted over a co-firing cluster (the
token-reduction + generalization-query win) and **hypothesis** memories recombining
distant pairs (A+B ⇒ likely-C). Both ASSERT claims, which is where FM1/FM2 and the
dream-cites-a-dream tower bite hardest — so the whole tier is QUARANTINED, SELF-DECAYING,
and OPT-IN:

  - **Behind a flag** (``HIPPO_DREAM_GENERATIVE``; default OFF — DREAM-KILL-1: never
    auto-applied as verified). ``dream --generate`` reports proposals anywhere; staging
    them needs the flag or the explicit ``--stage``.
  - **Created ONLY at ``confidence: draft``** — the staging path hard-codes the tier;
    a tampered proposal cannot land verified. Drafts enter recall DOWN-WEIGHTED (the
    ×0.5 quarantine weight recall applies since DRM-6 wired GOV-7's tier into ranking),
    are EXCLUDED from abstention-sensitive answering (a draft-only result set collapses
    back to the abstention shape), and are never floor material (``type: project``).
  - **Self-decaying**: a draft unconfirmed past ``DREAM_DRAFT_HORIZON`` distinct sessions
    auto-closes its validity window (``staleness.set_invalid_after`` — /dream's own
    artifact, reversible, ranking-only effect: the reversibility-gradient extension of
    DRM-4's "own un-aged edges may auto-retract") and AUTO-PROPOSES ARCHIVE; the archive
    itself (``--archive-draft``) stays a per-item act (inv4). A generation tier without
    this decay path must not ship — the sweep runs inside every apply pass whenever
    generated rows exist, independent of the flag staying on.
  - **Graduation is EXTERNAL-EVIDENCE-ONLY**: draft→verified happens exactly when the
    recorded outcome ledger (``outcome.injection_hits``, the KPI-2 join — the memory was
    injected AND one of its cited files was touched that session) carries evidence for
    the draft. Never a second human glance, never the pass grading its own output — the
    ONLY confidence write in this module is the evidence-gated branch in the sweep
    (asserted by test: no self-graduation path exists).
  - **The firewall extends to generative output** (inv-DRM-firewall): a draft is never
    replay SOURCE (seed or endpoint — ``dream.discover`` excludes the tier), a
    graduated-but-young generated memory stays excluded until it AGES IN
    (``dream.unaged_generated_stems``, same ``DREAM_AGE_SESSIONS`` window as edges), and
    the firewalled graph view drops quarantined NODES entirely so a draft parent's own
    wikilinks can never manufacture the next pass's bridge distances.
  - **Reversible-autonomy discipline carried over from DRM-2**: hard secret BLOCK on
    every generated byte (the ratified dream-write-path deviation from secrets.py's
    WARN-never-BLOCK), per-pass cap (``DREAM_MAX_GENERATE_PER_PASS`` ≤ 5), complete
    provenance in the committed ``dream-ledger.jsonl`` + an on-disk ``edge=`` stamp
    (doctor-reconciled), byte-exact undo (``--undo <id>`` deletes the staged file;
    refuse-on-drift protects a hand-edited or graduated draft), NEVER an autonomous
    commit, and a previously undone/archived/expired cluster is never auto re-staged
    (the ping-pong guard, keyed on the children set).

Measurement (the honesty surface):
  - **Prospective recall** — at first staging the current ``telemetry.abstention_backlog``
    is FROZEN under the derived dream dir; ``dream --prospective`` re-runs each frozen
    cluster's query and counts abstain→hit flips, attributing flips that surface a
    generated memory. The metric a schema tier must move.
  - **Graduation-rate alarm** — the sweep reports staged/graduated/expired/archived/
    undone counts and flags LOW graduation among decided drafts loudly: a generator
    whose claims the world never confirms is hallucinating, and the report says so.

Landing zone (re-verified against the live tree): generated drafts are staged INTO THE
CORPUS via ``new_memory.write_memory`` — the one canonical create primitive (refuses
overwrite, folds SEC-6 trust, refreshes the index) — because every quarantine mechanic
above (down-weight, abstention guard, decay, outcome join, prospective flips) operates on
RECALLABLE memories. The gitignored ``.claude/.memory-pending/`` queue the roadmap
snapshot named is the home for *unreviewed session text awaiting a human* (capture seeds,
SIG-6 fixture drafts); parking claims there would leave the tier inert — no recall
influence, no evidence, no decay, and the prospective-recall metric unsatisfiable. The
corpus + ``confidence: draft`` IS the quarantine.
"""

