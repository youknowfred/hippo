def draft_sweep_state(
    memory_dir: str, telemetry_dir: Optional[str] = None
) -> dict:
    """The sweep's PURE half: what would graduate, what has expired, and the alarm.

    Graduation is gated on EXTERNAL evidence only — ``outcome.injection_hits`` (the
    recorded-outcome join). There is deliberately NO other path to ``verified`` for a
    generated draft: not a pass re-reading its own output, not a human glance — evidence
    or the horizon. Read-only; never raises.
    """
    from .outcome import injection_hits

    td = telemetry_dir or default_telemetry_dir(memory_dir)
    out = {"rows": [], "graduate": [], "expire": [], "awaiting_archive": [], "alarm": {}}
    try:
        rows = generated_rows(memory_dir)
        if not rows:
            out["alarm"] = _alarm_stats(rows)
            return out
        distinct_now = int(soak_status(td, memory_dir=memory_dir).get("distinct_sessions") or 0)
        hits = injection_hits(memory_dir, td)
        horizon = draft_horizon()
        for row in rows:
            stem = row.get("memory")
            if row.get("state") != "active" or not isinstance(stem, str):
                continue
            live_conf = _live_confidence(memory_dir, stem)
            info = {
                "row": row,
                "stem": stem,
                "confidence": live_conf,
                "age": distinct_now - int(row.get("applied_at_distinct_count") or 0),
            }
            out["rows"].append(info)
            if live_conf != "draft":
                continue  # graduated already (or hand-regraded) — the horizon no longer applies
            ev = hits.get(stem)
            if ev and int(ev.get("hits") or 0) >= 1:
                out["graduate"].append({**info, "evidence": ev})
            elif row.get("expired"):
                out["awaiting_archive"].append(info)
            elif info["age"] >= horizon:
                out["expire"].append(info)
        out["alarm"] = _alarm_stats(rows)
        return out
    except Exception:
        return out


def _alarm_stats(rows: List[dict]) -> dict:
    """The graduation-rate / draft-reject hallucination alarm — proposal signal-to-noise.

    ``decided`` = drafts the world has ruled on (graduated / expired / archived /
    undone). A LOW graduation share among the decided means the generator asserts claims
    reality never confirms — the alarm names it loudly instead of letting quiet decay
    read as health."""
    staged = len(rows)
    graduated = sum(1 for r in rows if r.get("state") == "active" and r.get("confidence") == "verified")
    archived = sum(1 for r in rows if r.get("state") == "archived")
    undone = sum(1 for r in rows if r.get("state") == "undone")
    expired_pending = sum(
        1 for r in rows if r.get("state") == "active" and r.get("expired") and r.get("confidence") != "verified"
    )
    pending = sum(
        1
        for r in rows
        if r.get("state") == "active" and not r.get("expired") and r.get("confidence") != "verified"
    )
    decided = graduated + archived + undone + expired_pending
    rate = (graduated / decided) if decided else None
    return {
        "staged": staged,
        "graduated": graduated,
        "expired_pending_archive": expired_pending,
        "archived": archived,
        "undone": undone,
        "pending": pending,
        "decided": decided,
        "graduation_rate": (round(rate, 3) if rate is not None else None),
        "alarm": bool(decided >= _ALARM_MIN_DECIDED and rate is not None and rate < _ALARM_GRADUATION_FLOOR),
    }


def sweep_drafts(
    memory_dir: str,
    telemetry_dir: Optional[str] = None,
    index_dir: Optional[str] = None,
    *,
    repo_root: Optional[str] = None,
) -> Tuple[int, str]:
    """Execute the decay sweep: graduate evidence-confirmed drafts, expire horizon-old
    ones (auto-close their validity window — /dream's own artifact, reversible), and
    PROPOSE archives (per-item execution stays with ``--archive-draft``, inv4).

    Runs inside every apply pass whenever generated rows exist — the tier's self-decay
    never depends on the flag staying on — and stands alone as ``dream --sweep-drafts``.
    """
    from .staleness import set_invalid_after

    td = telemetry_dir or default_telemetry_dir(memory_dir)
    state = draft_sweep_state(memory_dir, td)
    alarm = state.get("alarm") or {}
    if not state["rows"] and not alarm.get("staged"):
        return 0, "🌱 dream drafts: none staged — nothing to sweep."

    now_iso = datetime.now(timezone.utc).isoformat(timespec="seconds")
    lines: List[str] = []
    ledger_appends: List[dict] = []
    wrote = False

    for item in state["graduate"]:
        stem, row, ev = item["stem"], item["row"], item["evidence"]
        res = _set_confidence(os.path.join(memory_dir, f"{stem}.md"), "verified")
        if res.get("error"):
            lines.append(f"  ✘ graduation of {stem} failed: {res['error']}")
            continue
        wrote = wrote or res.get("changed", False)
        ledger_appends.append(
            {
                "edge_id": row.get("edge_id"),
                "pass": row.get("pass"),
                "state": "active",
                "confidence": "verified",
                "graduated_at_ts": now_iso,
                "evidence": {"hits": ev.get("hits"), "sessions": ev.get("sessions")},
            }
        )
        lines.append(
            f"  ✓ GRADUATED {stem} draft→verified — recorded outcome evidence "
            f"({ev.get('hits')} hit session(s): {', '.join(ev.get('sessions') or [])}). "
            f"Still firewalled from /dream's own source set until it ages in."
        )

    for item in state["expire"]:
        stem, row = item["stem"], item["row"]
        res = set_invalid_after(os.path.join(memory_dir, f"{stem}.md"))
        if res.get("error"):
            lines.append(f"  ✘ expiry of {stem} failed: {res['error']}")
            continue
        wrote = True
        ledger_appends.append(
            {
                "edge_id": row.get("edge_id"),
                "pass": row.get("pass"),
                "state": "active",
                "expired": True,
                "expired_at_ts": now_iso,
            }
        )
        lines.append(
            f"  ⌛ EXPIRED {stem} — unconfirmed for {item['age']} distinct sessions "
            f"(horizon {draft_horizon()}): validity window closed (recall demotes it "
            f"further); ARCHIVE PROPOSED — execute with "
            f"`python -m memory.dream --archive-draft {stem}`."
        )

    for item in state["awaiting_archive"]:
        lines.append(
            f"  ⌛ {item['stem']} expired earlier and still awaits its per-item archive "
            f"(`--archive-draft {item['stem']}`)."
        )

    if ledger_appends:
        try:
            with open(apply_ledger_path(memory_dir), "a", encoding="utf-8") as fh:
                for row in ledger_appends:
                    fh.write(json.dumps(row, ensure_ascii=False) + "\n")
        except Exception as exc:
            return 1, f"🌱 dream drafts: sweep ledger append FAILED ({exc}) — re-run the sweep."
    if wrote:
        _refresh_index_quiet(memory_dir, index_dir)

    alarm = draft_sweep_state(memory_dir, td).get("alarm") or {}
    head = (
        f"🌱 dream drafts (generative tier): {alarm.get('staged', 0)} staged ever — "
        f"{alarm.get('pending', 0)} pending · {alarm.get('graduated', 0)} graduated · "
        f"{alarm.get('expired_pending_archive', 0)} expired-awaiting-archive · "
        f"{alarm.get('archived', 0)} archived · {alarm.get('undone', 0)} undone"
    )
    rate = alarm.get("graduation_rate")
    if rate is not None:
        head += f" · graduation_rate={rate}"
    lines.insert(0, head)
    if alarm.get("alarm"):
        lines.append(
            f"  🚨 HALLUCINATION ALARM: only {rate} of decided drafts earned outcome "
            f"evidence (floor {_ALARM_GRADUATION_FLOOR:.2f}) — the generator is asserting "
            "claims real work never confirms. Keep HIPPO_DREAM_GENERATIVE off, or raise "
            "θ / DREAM_SCHEMA_MIN_CLUSTER before staging more."
        )
    if not state["graduate"] and not state["expire"] and not state["awaiting_archive"]:
        lines.append("  nothing to graduate or expire this pass (evidence and horizon both quiet).")
    return 0, "\n".join(lines)


def archive_draft(
    memory_dir: str,
    name: str,
    *,
    repo_root: Optional[str] = None,
    telemetry_dir: Optional[str] = None,
    index_dir: Optional[str] = None,
) -> dict:
    """Execute ONE proposed draft archive (per-item, inv4). Refuses when ``name`` is not
    an ACTIVE dream-generated memory — non-generated memories use the normal audit
    archive flow, never this shortcut. Never raises."""
    from .archive import archive_memory

    result = {"name": name, "archived": False, "error": None}
    row = next(
        (r for r in generated_rows(memory_dir) if r.get("memory") == name and r.get("state") == "active"),
        None,
    )
    if row is None:
        result["error"] = (
            "not an ACTIVE dream-generated memory (see dream --log); for human memories "
            "use the audit archive flow"
        )
        return result
    rr = repo_root or os.path.dirname(os.path.dirname(os.path.abspath(memory_dir)))
    res = archive_memory(name, memory_dir, rr)
    if not res.get("moved"):
        result["error"] = res.get("error") or (
            f"refused — inbound referrers: {', '.join(res.get('referrers') or [])}"
        )
        return result
    try:
        with open(apply_ledger_path(memory_dir), "a", encoding="utf-8") as fh:
            fh.write(
                json.dumps(
                    {
                        "edge_id": row.get("edge_id"),
                        "pass": row.get("pass"),
                        "state": "archived",
                        "archived_at_ts": datetime.now(timezone.utc).isoformat(timespec="seconds"),
                    },
                    ensure_ascii=False,
                )
                + "\n"
            )
    except Exception as exc:
        result["error"] = f"archived on disk but the ledger append failed: {exc}"
        return result
    _refresh_index_quiet(memory_dir, index_dir)
    result["archived"] = True
    return result


