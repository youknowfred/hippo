# archive-proposed). 2× the edge aging window by default: a draft needs time to be
# injected and earn an outcome before the tier declares it unconfirmed.
_DEFAULT_DRAFT_HORIZON = 10

# Minimum co-firing cluster size for a schema parent — a pair is not a pattern; three
# mutually co-firing memories are the smallest cluster where one abstraction pays for
# its own injection tokens.
_DEFAULT_SCHEMA_MIN_CLUSTER = 3

# Maximum cluster size that still reads as ONE pattern. CALIBRATED 2026-07-12 on the
# live corpus: at θ=0.90 the dense release-history family fused into a single 19-member
# mutual component — a "schema" over 19 children is a haystack, not a gist, and its
# parent would be BORN over DRM-4's hub bar (DREAM_MAX_OUT_DEGREE default 8): staged
# pre-flagged as a parasite. The default deliberately equals that hub cap — the two
# counterweights agree by construction. An oversized component is reported as a
# θ-under-discriminates signal (raise DREAM_COFIRE_THETA), never staged.
_DEFAULT_SCHEMA_MAX_CLUSTER = 8

# Shared-vocabulary extraction: a token must appear in at least this many children's
