def _set_confidence(path: str, value: str, *, dry_run: bool = False) -> dict:
    """Rewrite ONE memory's ``confidence:`` frontmatter value (body byte-identical).

    The graduation primitive. Mirrors ``staleness.set_invalid_after``'s frontmatter
    discipline: metadata-nesting aware, refuses on missing/unparseable frontmatter,
    idempotent on the same value, never raises. Deliberately per-item, no batch.
    """
    result = {"path": path, "changed": False, "confidence": value, "error": None}
    try:
        with open(path, "r", encoding="utf-8") as fh:
            text = fh.read()
        if not text.startswith("---"):
            result["error"] = "no frontmatter"
            return result
        fm = parse_frontmatter(text)
        if not fm:
            result["error"] = "unparseable frontmatter -- refusing to write (fix the YAML)"
            return result
        lines = text.split("\n")
        close = next((i for i in range(1, len(lines)) if lines[i].strip() == "---"), None)
        if close is None:
            result["error"] = "no frontmatter"
            return result
        key_re = re.compile(r"^(\s*)confidence\s*:")
        idx = next((i for i in range(1, close) if key_re.match(lines[i])), None)
        if idx is None:
            # Insert via the ONE shared walk (COR-9/COR-14) — nested under metadata:
            # when present, at that block's OWN child indent. This writer used to
            # hard-code two spaces, so a hand-reformatted draft (4-space children)
            # became a document that no longer parses.
            from .provenance import insert_frontmatter_keys

            lines[1:close] = insert_frontmatter_keys(
                lines[1:close], [f"confidence: {value}"]
            )
        else:
            indent = key_re.match(lines[idx]).group(1)
            new_line = f"{indent}confidence: {value}"
            if lines[idx] == new_line:
                return result  # idempotent
            lines[idx] = new_line
        new_text = "\n".join(lines)
        # COR-14: the write-site damage check every frontmatter writer answers — did
        # this rewrite touch any key it does not own, or stop the document parsing?
        from .provenance import _frontmatter_damage

        damage = _frontmatter_damage(text, new_text, {"confidence"})
        if damage:
            result["error"] = (
                f"refusing to rewrite {os.path.basename(path)}: {damage} — "
                "this is a hippo bug, please report it"
            )
            return result
        result["changed"] = new_text != text
        if result["changed"] and not dry_run:
            with open(path, "w", encoding="utf-8") as fh:
                fh.write(new_text)
            try:
                from .trust import record_authored_write

                record_authored_write(os.path.dirname(path), path)
            except Exception:
                pass
    except Exception as exc:
        result["error"] = str(exc)
    return result


def _set_cited_paths(path: str, paths: List[str], *, dry_run: bool = False) -> dict:
    """Set ONE memory's ``cited_paths`` frontmatter to ``paths`` (body byte-identical).

    Staging-time only, and only when the canonical backfill left the key missing/empty —
    the draft's outcome surface must exist for graduation evidence to ever accrue. Same
    frontmatter discipline as ``_set_confidence``; drops block-style continuation lines
    exactly like ``links.add_typed_relation`` (values re-rendered as one flow list).
    """
    result = {"path": path, "changed": False, "error": None}
    try:
        with open(path, "r", encoding="utf-8") as fh:
            text = fh.read()
        if not text.startswith("---"):
            result["error"] = "no frontmatter"
            return result
        if not parse_frontmatter(text):
            result["error"] = "unparseable frontmatter -- refusing to write (fix the YAML)"
            return result
        lines = text.split("\n")
        close = next((i for i in range(1, len(lines)) if lines[i].strip() == "---"), None)
        if close is None:
            result["error"] = "no frontmatter"
            return result
        value = "[" + ", ".join(json.dumps(p) for p in paths) + "]"
        key_re = re.compile(r"^(\s*)cited_paths\s*:")
        idx = next((i for i in range(1, close) if key_re.match(lines[i])), None)
        if idx is None:
            # COR-14: same shared insert walk as _set_confidence — indent read from
            # the metadata block's own keys, never hard-coded.
            from .provenance import insert_frontmatter_keys

            lines[1:close] = insert_frontmatter_keys(
                lines[1:close], [f"cited_paths: {value}"]
            )
        else:
            indent = key_re.match(lines[idx]).group(1)
            end = idx + 1
            while end < close and re.match(r"^\s+-\s", lines[end]):
                end += 1
            lines[idx:end] = [f"{indent}cited_paths: {value}"]
        new_text = "\n".join(lines)
        from .provenance import _frontmatter_damage

        damage = _frontmatter_damage(text, new_text, {"cited_paths"})
        if damage:
            result["error"] = (
                f"refusing to rewrite {os.path.basename(path)}: {damage} — "
                "this is a hippo bug, please report it"
            )
            return result
        result["changed"] = new_text != text
        if result["changed"] and not dry_run:
            with open(path, "w", encoding="utf-8") as fh:
                fh.write(new_text)
    except Exception as exc:
        result["error"] = str(exc)
    return result


