def freeze_abstention_backlog(telemetry_dir: str) -> dict:
    """Freeze the CURRENT abstention backlog under the derived dream dir (inv1) — once.

    An existing frozen file is kept (frozen means frozen: the baseline predates the
    staged drafts, which is exactly what makes a later flip attributable). Returns the
    frozen payload. Never raises; ``{"clusters": []}`` on failure."""
    from .telemetry import abstention_backlog

    try:
        path = frozen_backlog_path(telemetry_dir)
        if os.path.isfile(path):
            with open(path, "r", encoding="utf-8") as fh:
                payload = json.load(fh)
            if isinstance(payload, dict) and isinstance(payload.get("clusters"), list):
                return payload
        clusters = abstention_backlog(telemetry_dir)
        payload = {
            "frozen_at_ts": datetime.now(timezone.utc).isoformat(timespec="seconds"),
            "clusters": [
                {
                    "sample_query": c.get("sample_query"),
                    "count": c.get("count"),
                    "terms": list(c.get("terms") or []),
                }
                for c in clusters
            ],
        }
        os.makedirs(dream_dir(telemetry_dir), exist_ok=True)
        with open(path, "w", encoding="utf-8") as fh:
            json.dump(payload, fh, ensure_ascii=False, indent=1)
        return payload
    except Exception:
        return {"clusters": []}


def prospective_recall(
    memory_dir: str,
    telemetry_dir: Optional[str] = None,
    index_dir: Optional[str] = None,
    *,
    k: int = 10,
) -> dict:
    """The DRM-6 metric: over the FROZEN backlog, which recorded abstentions now HIT —
    and which flips surface a dream-generated memory (attribution).

    Offline, measure-only (inv6): the same supplied-index + index_dir call shape the
    eval metrics use (graph expansion + the DRM-6 quarantine guard both active), never
    the hot path, never a write. ``{"frozen_at_ts", "clusters", "flips",
    "flips_via_dream", "per_query": [...]}``; a missing frozen baseline reports itself.
    """
    from .build_index import default_index_dir, load_index
    from .recall import recall

    td = telemetry_dir or default_telemetry_dir(memory_dir)
    out = {"frozen_at_ts": None, "clusters": 0, "flips": 0, "flips_via_dream": 0, "per_query": []}
    try:
        path = frozen_backlog_path(td)
        if not os.path.isfile(path):
            out["note"] = (
                "no frozen abstention baseline yet — it freezes automatically at first "
                "staging, or freeze explicitly via dream --generate"
            )
            return out
        with open(path, "r", encoding="utf-8") as fh:
            payload = json.load(fh)
        clusters = payload.get("clusters") or []
        out["frozen_at_ts"] = payload.get("frozen_at_ts")
        out["clusters"] = len(clusters)
        idx_dir = index_dir or default_index_dir(memory_dir)
        idx = load_index(idx_dir)
        if idx is None or not len(idx):
            out["note"] = "no recall index — build one first (nothing measured)"
            return out
        known = {e.get("name") for e in idx.entries}
        dream_stems = {
            r.get("memory") for r in generated_rows(memory_dir) if r.get("state") == "active"
        }
        for c in clusters:
            q = (c.get("sample_query") or "").strip()
            if not q:
                continue
            res = recall(q, k=k, index=idx, index_dir=idx_dir)
            names = [r.get("name") for r in res if r.get("name") in known]
            hit = bool(names)
            via_dream = bool(set(names) & dream_stems)
            out["flips"] += 1 if hit else 0
            out["flips_via_dream"] += 1 if via_dream else 0
            out["per_query"].append(
                {
                    "query": q,
                    "count": c.get("count"),
                    "hit": hit,
                    "via_dream": via_dream,
                    "hits": names[:5],
                }
            )
        return out
    except Exception:
        return out


def render_prospective(metric: dict) -> str:
    lines = [
        f"🔭 prospective recall — frozen abstention backlog "
        f"({metric.get('clusters', 0)} cluster(s), frozen {metric.get('frozen_at_ts') or 'never'}): "
        f"{metric.get('flips', 0)} abstain→hit flip(s), {metric.get('flips_via_dream', 0)} "
        f"surfacing a dream-generated memory"
    ]
    if metric.get("note"):
        lines.append(f"   {metric['note']}")
    for row in metric.get("per_query") or []:
        mark = "✓" if row.get("hit") else "·"
        via = " ★via-dream" if row.get("via_dream") else ""
        lines.append(
            f"   {mark} \"{row.get('query', '')[:60]}\" (asked {row.get('count')}×)"
            + (f" → {', '.join(row.get('hits') or [])}" if row.get("hit") else " → still abstains")
            + via
        )
    if not metric.get("per_query"):
        lines.append("   (empty backlog — no recurring abstentions were recorded before staging)")
    return "\n".join(lines)


# --------------------------------------------------------------------------- #
# Pass orchestration (called from memory.dream's CLI / apply pass / MCP)
# --------------------------------------------------------------------------- #
def run_generative_pass(
    memory_dir: str,
    index_dir: Optional[str] = None,
    telemetry_dir: Optional[str] = None,
    *,
    stage: bool = False,
    probe_k: Optional[int] = None,
    max_seeds: Optional[int] = None,
    repo_root: Optional[str] = None,
    result: Optional[dict] = None,
) -> Tuple[int, str]:
    """Discover → cluster → propose → (optionally) stage, with the decay sweep first.

    ``stage=False`` is REPORT-ONLY end to end: proposals print + land under the derived
    dream dir, zero memory writes, no sweep (a report pass must not write). ``stage=True``
    re-checks the SEC-1 trust gate, runs the decay sweep (graduate/expire), then stages
    up to the cap. ``result`` lets the apply pass hand over its own discovery result so
    the corpus is probed once per pass, not twice.
    """
    from .dream import discover

    td = telemetry_dir or default_telemetry_dir(memory_dir)
    lines: List[str] = []

    if stage:
        from . import trust

        gate_root = trust.gate_repo_root(memory_dir, repo_root)
        if gate_root is not None and not trust.is_trusted(gate_root):
            return 1, (
                "🌱 dream --generate: STAGING REFUSED — this corpus is untrusted (SEC-1). "
                "The report-only form (no --stage) remains available."
            )
        if generated_rows(memory_dir):
            _sc, sweep_text = sweep_drafts(memory_dir, td, index_dir, repo_root=repo_root)
            lines.append(sweep_text)

    if result is None:
        result = discover(memory_dir, index_dir, td, probe_k=probe_k, max_seeds=max_seeds)
    if result.get("status") != "ok":
        lines.append(
            f"🌱 dream --generate {result.get('pass_id', '?')} — no proposals: {result.get('reason')}"
        )
        return (1 if result.get("status") == "no-index" else 0), "\n".join(lines)

    texts: Dict[str, str] = {}
    try:
        from .dream import _corpus_texts

        texts = _corpus_texts(memory_dir)
    except Exception:
        pass
    proposals = propose_generative(result, texts)
    pass_id = result.get("pass_id", "?")
    ledger_path = write_proposals_ledger(td, pass_id, proposals)
    n_s, n_h = len(proposals["schemas"]), len(proposals["hypotheses"])

    lines.append(
        f"🌱 dream generative pass {pass_id} — {n_s} schema / {n_h} hypothesis proposal(s) "
        f"(flag {'ON' if generative_enabled() else 'OFF'}; mode: {'STAGE' if stage else 'report-only'})"
    )
    if ledger_path:
        lines.append(f"   proposals ledger: {ledger_path}")
    if n_s == n_h == 0:
        lines.append(
            "   empty — no mutual co-firing cluster (≥"
            f"{schema_min_cluster()}) or distant pair above θ this pass (the norm)."
        )
    for p in proposals["schemas"] + proposals["hypotheses"]:
        lines.append(
            f"   • {p['kind']:<10} {p['name']}  cofire={p['cofire']:.2f}  "
            f"children: {', '.join(p['children'])}"
        )
    for big in proposals.get("oversized") or []:
        lines.append(
            f"   ⚠ oversized cluster SKIPPED ({len(big)} members > "
            f"{schema_max_cluster()}): {', '.join(big[:6])}… — θ under-discriminates at "
            "this density; a schema over it would be a haystack (and born over DRM-4's "
            "hub bar). Raise DREAM_COFIRE_THETA before staging."
        )

    if stage and (n_s or n_h):
        freeze_abstention_backlog(td)  # the prospective baseline predates the staging
        staged = stage_generated(
            memory_dir,
            proposals,
            pass_id=pass_id,
            telemetry_dir=td,
            index_dir=index_dir,
            repo_root=repo_root,
        )
        for row in staged["staged"]:
            lines.append(
                f"   ✚ STAGED {row['memory']} ({row['kind']}, confidence: draft, "
                f"[{row['edge_id']}]) — down-weighted in recall, expires at horizon "
                f"{draft_horizon()} unless outcome evidence graduates it; undo with "
                f"`python -m memory.dream --undo {row['edge_id']}`"
            )
        for prop, reason in staged["refused"]:
            lines.append(f"   ✘ refused {prop.get('name', '?')}: {reason}")
        if not staged["staged"]:
            lines.append("   staged 0 (cap/guards) — see refusals above." if staged["refused"] else "   staged 0.")
        lines.append(
            "   commit stays yours (never auto-committed); drafts never answer alone and "
            "are firewalled from /dream's own source set."
        )
    elif n_s or n_h:
        lines.append(
            "   report-only: stage with `dream --generate --stage` (explicit) or set "
            "HIPPO_DREAM_GENERATIVE=1 to let apply passes stage up to the cap."
        )
    return 0, "\n".join(lines)
